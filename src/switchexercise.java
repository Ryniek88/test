public class switchexercise {
    public static void main(String[] args) {
        char litera = 'C';
        switch (litera){
            case 'A': System.out.println("Able");
            break;
            case 'B': System.out.println("Bravo");
            break;
            default: System.out.println("nic nie ma");
            break;
        }
    }
}
